    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        if (existe)
        {
            try
            {
                if (Eje5.buscarinscri(String.valueOf(jcNombreE.getSelectedItem()),tfDni.getText().toUpperCase()))
                    throw new Exception("ESTA PERSONA YA ESTA INSCRITA EN ESTE EVENTO");
                
                Eje5.insertarasistencia(String.valueOf(jcNombreE.getSelectedItem()),tfDni.getText().toUpperCase(),this);           
            }
            catch (Exception e)
            {
                showMessageDialog(this,e.getMessage());
                Eje5.salirv(this);
            }
        }
        else
        {
            try
            {
                Eje5.insertarpersona(tfDni.getText().toUpperCase(),tfNombreP.getText(),tfPApellido.getText(),tfSApellido.getText(),tfTelefono.getText());
                Eje5.insertarasistencia(String.valueOf(jcNombreE.getSelectedItem()), tfDni.getText(), this);
            }
            catch (Exception e)
            {
                showMessageDialog(this,e.getMessage());
            }
        }
    }//GEN-LAST:event_bAceptarActionPerformed

